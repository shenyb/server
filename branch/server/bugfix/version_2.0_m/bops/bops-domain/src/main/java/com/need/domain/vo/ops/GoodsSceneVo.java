package com.need.domain.vo.ops;

public class GoodsSceneVo {
	 private String goodsId;

	 private String goodsName;
	 
	 private String topPicKeys;
	 
	 private String auditStatus;
	   
	 private String scenesVal;
	 private String reason;
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getTopPicKeys() {
		return topPicKeys;
	}
	public void setTopPicKeys(String topPicKeys) {
		this.topPicKeys = topPicKeys;
	}
	public String getAuditStatus() {
		return auditStatus;
	}
	public void setAuditStatus(String auditStatus) {
		this.auditStatus = auditStatus;
	}

	public String getScenesVal() {
		return scenesVal;
	}
	public void setScenesVal(String scenesVal) {
		this.scenesVal = scenesVal;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	 
}
